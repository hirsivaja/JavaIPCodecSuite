sealed trait IpProtocol {
  def protocol: Byte
}

object IpProtocol {
  def fromType(protocolType: Byte): IpProtocol = {
    IpProtocols.values.find(value => protocolType == value.protocol).getOrElse(GenericIpProtocol(protocolType))
  }
}

